package edu.kit.kastel.monstercompetition.model.game;

/**
 * An example enum representing the phases described.
 *
 * @author uozqc
 */
public enum Phase {
    /**
     * PHASE0: check if the competition ends.
     */
    PHASE0,
    /**
     * PHASEI: each monster chooses an action.
     */
    PHASEI,
    /**
     * PHASEII: monsters execute actions in SPD order.
     */
    PHASEII
}
